a = 31 
t = type(a) # Output: <class 'int'>

print(t) # This will print the type of variable 'a'

b = "31"
c = type(b) # Output: <class 'str'>

print(c) # This will print the value of variable 'b'

d = float(b) # Convert string to float
e = type(d) # Output: <class 'float'>
print(e) # This will print the type of variable 'd'