#Arithmetic Operators

a = 34
b = 23

c = a + b
print("Addition:", c)

#Assignment Operators

a = 4-2 #Assign the result of 4-2 to a
print(a)

b = 6
b +=3 #Increment b by 3
print(b)

#Comparison Operators

d = 5>4
print(d)  # True if 5 is greater than 4

#Logical Operators

e = True or False
print(e)  # True because one of the operands is True    

#Truth Table of 'or' Operator
print("True or False is", True or False)  # True
print("True or True is", True or True)    # True
print("False or False is", False or False)  # False 
print("False or True is", False or True)  # True

#Truth Table of 'and' Operator
print("True and False is", True and False)  # False 
print("True and True is", True and True)    # True
print("False and True is", False and False)  # False 
print("False and False is", False and True)  # False

#Truth Table of 'not' Operator
print("not True is", not True)  # False
print("not False is", not False)  # True
